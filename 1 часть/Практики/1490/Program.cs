using System;

public class Program
{
    public static void Main()
    {
        long R = long.Parse(Console.ReadLine());
        
        // Плита портится, если хотя бы один ее угол находится
        // на расстоянии ≤ R от центра
        
        // Вместо перебора всех плит, переберем все возможные
        // координаты углов (x,y) где x,y целые
        
        // Каждая целая точка (x,y) внутри круга соответствует
        // 4 плитам, для которых она является углом
        // Но плиты могут учитываться несколько раз
        
        // Более простой подход: плита с центром в (i+0.5, j+0.5)
        // портится, если ее центр находится на расстоянии ≤ R+√2/2
        
        // Но точнее: плита портится, если минимальное расстояние
        // от центра до плиты ≤ R
        
        // Для плиты [i,i+1]×[j,j+1], минимальное расстояние от (0,0):
        // dx = max(0, |i| - 1) если i и i+1 одного знака, иначе 0
        // dy аналогично
        
        // Минимальное расстояние² = dx² + dy²
        
        long result = 0;
        
        for (long i = -R; i < R; i++)
        {
            // Вычисляем dx для плиты с x-координатами [i,i+1]
            long dx2;
            if (i >= 0)
                dx2 = i * i;
            else if (i + 1 <= 0)
                dx2 = (i + 1) * (i + 1);
            else
                dx2 = 0;
            
            // Максимальное dy² такое, что плита еще касается круга
            long maxDy2 = R * R - dx2;
            if (maxDy2 < 0) continue;
            
            // Максимальное |j| такое, что плита касается
            long maxJ = (long)Math.Floor(Math.Sqrt(maxDy2));
            
            // Для данного i, плиты с j от -maxJ до maxJ-1 портятся
            result += 2 * maxJ;
        }
        
        Console.WriteLine(result);
    }
}